class SkillDefinitionService {
    /**
     * Get all available skill trees
     */
    async getSkillTrees() {
        logger.info('get_all_skill_trees');
        return exports.SKILL_TREES;
    }
    /**
     * Get a specific skill tree by category
     */
    async getSkillTree(category) {
        const tree = exports.SKILL_TREES.find((t) => t.category === category);
        if (!tree) {
            logger.warn('skill_tree_not_found', { category });
            return null;
        }
        return tree;
    }
    /**
     * Get a skill tree by its ID
     */
    async getSkillTreeById(treeId) {
        const tree = exports.SKILL_TREES.find((t) => t.id === treeId);
        if (!tree) {
            logger.warn('skill_tree_not_found_by_id', { treeId });
            return null;
        }
        return tree;
    }
    /**
     * Find a node by ID across all trees
     */
    findNodeById(nodeId) {
        for (const tree of exports.SKILL_TREES) {
            const node = tree.nodes.find((n) => n.id === nodeId);
            if (node) {
                return { tree, node };
            }
        }
        logger.warn('node_not_found', { nodeId });
        return null;
    }
    /**
     * Get category info for display
     */
    getCategoryInfo(category) {
        return (exports.SKILL_TREE_CATEGORIES[category] ?? {
            label: category,
            icon: 'help-outline',
            color: '#6B7280',
        });
    }
    /**
     * Get all categories
     */
    getAllCategories() {
        return Object.keys(exports.SKILL_TREE_CATEGORIES);
    }
    /**
     * Validate that all prerequisite references are valid within a tree
     */
    validateTreeStructure(tree) {
        const nodeIds = new Set(tree.nodes.map((n) => n.id));
        for (const node of tree.nodes) {
            for (const prereqId of node.prerequisites) {
                if (!nodeIds.has(prereqId)) {
                    logger.error('invalid_prerequisite_reference', {
                        treeId: tree.id,
                        nodeId: node.id,
                        prereqId,
                    });
                    return false;
                }
            }
        }
        return true;
    }
}
// Export singleton instance
exports.skillDefinitionService = new SkillDefinitionService();
